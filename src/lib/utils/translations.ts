/**
 * Get a translated string by key
 * @param language - Language code (e.g., 'en', 'es')
 * @param key - Dot-notation key (e.g., 'tablet.travelLogs')
 * @returns The translated string
 */
export function getTranslation(language: Language, key: TranslationKey): string {
	const keys = key.split('.');
	let value: any = translations[language];

	for (const k of keys) {
		value = value?.[k];
	}

	return typeof value === 'string' ? value : key;
}

/**
 * Get a translated string by key and interpolate template variables
 * Example: key 'entry.welcome' -> 'WELCOME {firstName} {lastName}!' with values { firstName, lastName }
 */
export function getTranslationWithValues(
	language: Language,
	key: TranslationKey,
	values: Record<string, string | number>
): string {
	const template = getTranslation(language, key);
	return String(template).replace(/\{(\w+)\}/g, (_, k) => String(values[k] ?? `{${k}}`));
}

/**
 * Get localized character name with English fallback
 */
export function getCharacterName(
	language: Language,
	key: keyof typeof translations['en']['names']
): string {
	const localized = (translations[language] as any)?.names?.[key];
	if (typeof localized === 'string') return localized;
	const fallback = translations['en']?.names?.[key];
	return typeof fallback === 'string' ? fallback : key;
}
